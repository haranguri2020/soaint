package com.carrito.servicios.model.socio;

public class SocioResponse {

	private String priNomt;
	private String segNomt;
	private String apePat;
	private String apeMat;
	private String nomCompleto;
	private String tlfCelc;
	private String email;
	
	public String getPriNomt() {
		return priNomt;
	}
	public void setPriNomt(String priNomt) {
		this.priNomt = priNomt;
	}
	public String getSegNomt() {
		return segNomt;
	}
	public void setSegNomt(String segNomt) {
		this.segNomt = segNomt;
	}
	public String getApePat() {
		return apePat;
	}
	public void setApePat(String apePat) {
		this.apePat = apePat;
	}
	public String getApeMat() {
		return apeMat;
	}
	public void setApeMat(String apeMat) {
		this.apeMat = apeMat;
	}
	public String getTlfCelc() {
		return tlfCelc;
	}
	public void setTlfCelc(String tlfCelc) {
		this.tlfCelc = tlfCelc;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getNomCompleto() {
		return nomCompleto;
	}
	public void setNomCompleto(String nomCompleto) {
		this.nomCompleto = nomCompleto;
	}

	 
}
